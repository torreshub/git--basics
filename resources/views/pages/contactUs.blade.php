<p>How to apply to become Acceptance Test Employer?
<p>Requirements for the Employer Application:
If the employer applicant or any of the owners, officers or partners of the employer had a contractor license or business
    license suspended or revoked any time in the last five years, they are not eligible to become a NLCAA Employer.
If the Employer applicant or any of the its owners, officers or partners ever been found liable in a civil suit or found
   <p>guilty in a criminal action for fraud, theft, or any other acts of dishonesty, they are not eligible to become a NLCAA Employer.
The Employer applicant must have applicable business license(s) or business tax certificate(s) number for all offices that
  <p>  are registering to employed acceptance test technician.
NLCAA Employers must have a compressive general liability insurance policy with a policy limit of at least $1,000,000.
NLCAA Employers must have workers compensation insurance. Provide a copy with application.
