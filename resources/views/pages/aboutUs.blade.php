Acceptance Test Technician is a process of testing lighting controls to see if they are in compliance with Title 24 Part 6 Mandatory Lighting Controls.

In November 2014 NLCAA, Inc. was approved by the California Energy Commission as Acceptance Test Technician Certified Provider.

The NLCAA mission is to fill the growing need in the Energy Efficiency industry for a technically superior, yet customer-oriented
    as an Acceptance Test Certified Provider.
