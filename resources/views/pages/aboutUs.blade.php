Acceptance Test Software
<p>
NLCAA, Inc. has developed and owns its own software that resides in our cloud which has been reviewed by the California Energy Commission Staff.
<ul>
<li>The software is utilized by the Acceptance Test Technician to enter testing data with a IPAD, tablet, and or smart phone.</li>
    <li>All required forms are generated by the software.</li>
    <li>The required test calculations are embedded in the software to eliminate mathematical errors.</li>
    <li>The software provides taking pictures of the devices tested for quality assurance.</li>
    <li>Required signatures for the Declaration Form will be entered by using a stylus from any location.</li>
    <li>Certificates of Acceptance will be automatically email to building inspector provided that you enter their email address.</li>
NLCAA, Inc. maintains the computer based registry of data as a repository of information for the required 3 years that can be accessed by the ATTs, building departments, contractors and government agencies.
You may print out all the forms.</p></ul>
